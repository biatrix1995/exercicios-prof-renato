alert(" Olá! Vamos fazer o primeiro cálculo! ")
let valorBase = Number (prompt (" Insira o valor da base do seu retângulo "))
let valorAltura = Number (prompt (" Insira o valor da altura do seu retângulo "))
let valorArea = valorBase  *  valorAltura
alert(" O valor da área do seu retângulo é de " + valorArea )
let valorPerimetro = valorArea * 2 
alert(" O valor do perímetro do seu retângulo é " + valorPerimetro )
